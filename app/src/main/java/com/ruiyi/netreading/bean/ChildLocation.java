package com.ruiyi.netreading.bean;

//步骤分标签坐标位置
public class ChildLocation {
    private float x;
    private float y;
    private String tv; //分值

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }

    public String getTv() {
        return tv;
    }

    public void setTv(String tv) {
        this.tv = tv;
    }
}
